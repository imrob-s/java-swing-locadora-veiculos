    private void salvar() {
        FabricanteDTO fabricante = new FabricanteDTO();
        FabricanteService service = new FabricanteService();
        
        fabricante.setNome(txtFabricante.getText());
        if(!txtFabricante.getText().isBlank()){
        try {
            service.save(fabricante);
            Application.listaFabricante = service.findAll();
            
            JOptionPane.showMessageDialog(null, "Fabricante salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            Utils.sair(this);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar o fabricante no sistema: " + ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        } else {
            JOptionPane.showMessageDialog(null, "Obrigat√≥rio preencher todos os campos!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

